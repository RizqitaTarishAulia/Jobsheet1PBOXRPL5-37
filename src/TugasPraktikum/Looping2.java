public class Looping2 {
    public static void main(String[]args){
        
        int x;
        for(x=1;x<=1;x++){
            System.out.print("@");
        }
        System.out.println();
        for(x=1;x<=2;x++){
            System.out.print("@");
        }
        System.out.println();
        for(x=1;x<=3;x++){
            System.out.print("@");
        }
        System.out.println();
        for(x=1;x<=4;x++){
            System.out.print("@");
        }
        System.out.println();    
    }
}
